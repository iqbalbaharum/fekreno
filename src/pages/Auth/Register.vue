<template>
  <div class="fullscreen row">
    <div class="col bg-primary"></div>
    <div class="col"></div>
    <div class="fullscreen flex flex-center">
      <q-card class="q-pa-md" style="width: 400px">
        <div class="text-weight-bold text-center q-pb-lg q-gutter-sm">
          <q-img class="q-mb-md" src="~assets/krenovator.png" />
          <div class="text-h4 text-primary">Create Account</div>
          <div class="text-caption">
            Dont have an account? Create your account quickly
          </div>
        </div>

        <div class="q-gutter-sm">
          <q-input
            filled
            v-model.trim="form.name"
            label="Username"
            ref="name"
            error-message="Username can't be empty"
            :error="$v.form.name.$error"
            @keydown.space.prevent
          />

          <q-input
            filled
            v-model.trim="form.email"
            label="Email Address"
            ref="email"
            :error="$v.form.email.$error"
            error-message="Enter a valid email"
          />

          <q-input
            filled
            v-model.trim="form.mobile"
            label="Mobile"
            ref="mobile"
            :error="$v.form.mobile.$error"
            error-message="Enter a valid mobile"
          />

          <q-input
            filled
            v-model="form.password"
            :type="type"
            ref="password"
            label="Create Your Own Password"
            :error="$v.form.password.$error"
            error-message="Password can't be empty"
          >
            <template v-slot:append>
              <q-btn
                round
                flat
                icon="remove_red_eye"
                @click="onClickShowPassword"
              />
            </template>
          </q-input>

          <div>
            <div class="text-caption">
              By clicking Register, you have agreed with our Term of Conditions
              of usage and using cookies.
            </div>
          </div>

          <div class="q-mt-md q-gutter-sm text-center">
            <q-btn
              color="primary"
              text-color="white"
              label="Register"
              no-caps
              @click="onClickRegister"
            />

            <q-btn
              color="primary"
              text-color="primary"
              outline
              unelevated
              to="/login"
              label="Back to Login"
              no-caps
            />
          </div>
        </div>
      </q-card>

      <prompt
        :show="dialog.success"
        boxtype="success"
        :buttons="['continue']"
        icon="fas fa-check-circle"
        title="Registration Successful"
        body="Congratulations, your account has been succesfully created"
        @continue="onClickContinue"
      />
      
      <prompt
        :show="dialog.alert"
        boxtype="alert"
        :buttons="['ok']"
        icon="fas fa-bomb"
        title="Registration Error"
        body="Oh no, please contact customer service"
        @ok="onClickOk"
      />

    </div>
  </div>
</template>

<script>
import { minLength, required, email } from 'vuelidate/lib/validators';
import Prompt from './../../components/Prompt';

export default {
  data() {
    return {
      form: {
        name: '',
        password: '',
        email: '',
        mobile: ''
      },
      type: 'password',
      dialog: {
        success: false,
        alert: true,
      }
    };
  },

  components: {
    Prompt
  },

  validations: {
    form: {
      name: { required },
      email: { required, email },
      password: { required },
      mobile: { required }
    }
  },

  methods: {
    async onClickRegister() {
      this.$v.form.$touch();

      if (this.$v.form.$invalid) {
        return this.dialog.alert = true
      }

      try {
        let res = await this.$store.dispatch('RegisterIndividual', this.form);
        this.dialog.success = true;
      } catch (err) {
        if (err) {
          this.dialog.alert = true;
        }
      }
    },

    onClickShowPassword() {
      if (this.type === 'password') {
        this.type = 'text';
      } else {
        this.type = 'password';
      }
    },

    onClickOk() {
      this.dialog.alert = false;
    },

    onClickContinue() {
      this.$router.push('/login');
    }
  }
};
</script>
